#include <stdio.h>

int	ft_str_is_alpha(char *str)
{
	int	i;
	int	c;

	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		if ((c < 'a' || c > 'z') && (c < 'A' || c > 'Z'))
			return (0);
		i++;
	}
	return (1);
}
